public class Address {
    String city;
    String postalCode;
    String street;
    String streetNumber;
    String flatNumber;

    public Address(String city, String postalCode, String street, String streetNumber, String flatNumber) {
        this.city = city;
        this.postalCode = postalCode;
        this.street = street;
        this.streetNumber = streetNumber;
        this.flatNumber = flatNumber;
    }
}
